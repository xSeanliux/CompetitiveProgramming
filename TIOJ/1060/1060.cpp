int g(int z){
    //cout << "Running g(" << z << ")" << endl;
    if(z <= 2) return z*z - 1;
    else return 2;
}

int h(int y){
    //cout << "Running h(" << y << ")" << endl;
    if(y < 2) return -1;
    else {
        y %= 6;
        if(y <= 1) return -1;
        else if(y == 2 || y == 5) return 2;
        else return 5;
    }
}

int f(int x){
    if(fVal.count(x)) return fVal[x];
    //cout << "Running f(" << x <<")"<< endl;
    if(x > h(x)){
        fVal.insert(pair<int, int>(x, f(x-1) - h(x)));
    } else if(x < h(x)){
        fVal.insert(pair<int, int>(x, f(g(x)) - g(x)));
    } else {
        fVal.insert(pair<int, int>(x, 1));
    }
    return fVal[x];

}

